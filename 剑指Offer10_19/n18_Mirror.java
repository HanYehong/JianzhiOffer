package 剑指Offer10_19;

/**
 * 操作给定的二叉树，将其变换为源二叉树的镜像。
 * 
 * @author HanYehong
 *
 *@Time 2018-9-4
 */
public class n18_Mirror {
	public void Mirror(TreeNode root) {
        TreeNode temp;
        if(root!=null)
        {
        temp=root.left;
        root.left=root.right;
        root.right=temp;
        if(root.left!=null)
            Mirror(root.left);
        if(root.right!=null)
            Mirror(root.right);
        }
    }

}
